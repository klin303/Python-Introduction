# while_loops.py
# Purpose: learn how to use while loops

# Loops in coding are used to execute the same lines of code over and over

# the simplist of these is the while loop

# while (condition):
#     execute

x = 0

while x < 5: # will run until x >= 5
    print(x)
    x += 1 # same as x = x + 1
print("loop completed\n")


y = 0
while y < 0:
    print(y)
    y += 1
print("loop completed\n")


