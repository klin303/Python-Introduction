# Intro to Variables

# a variable is a container for data

# in python, when you declare a variable, you don't need to specify what type

# we already talked about strings as a data type: "word"
# and chars: 'a'

# now we have ints - whole numbers negative or positive (no decimals)

# print(x) this will give us an ERROR since x is not defined
x = 1
print(x)

x = -1
print(x)

name = "Kenny"  # we can also store strings
print(name)

# another cool thing we can do with variables is math
x = 3
y = 5
z = x + y
print(z)

y = 10
print(z)

z = x + y
print(z)

x = x + 1
print(x)

# we can also store input in variables
# input is a way for the user of the program to pass information or data to the program

userInput = input()
print(userInput)

# in this case, input will be from the terminal (where the output is normally displayed)


